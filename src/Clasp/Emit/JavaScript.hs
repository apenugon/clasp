emitModule :: LowerModule -> Text
emitModule modl =
  T.unlines $
    [ "// Generated by claspc"
    , "// Source module: " <> unModuleName (lowerModuleName modl)
    ]
      <> emitRuntimePrelude
      <> emitPrimitiveCodecHelpers
      <> emitListCodecHelpers (lowerModuleCodecTypes modl)
      <> emitRequestSchemaHelpers (lowerModuleTypeDecls modl) (lowerModuleRecordDecls modl)
      <> concatMap emitTypeCodecHelpers (lowerModuleTypeDecls modl)
      <> concatMap emitRecordCodecHelpers (lowerModuleRecordDecls modl)
      <> emitForeignBindings (lowerModuleTypeDecls modl) (lowerModuleRecordDecls modl) (lowerModuleForeignDecls modl)
      <> emitNativeInteropExport (lowerModuleForeignDecls modl)
      <> snd (emitDecls 0 (lowerModuleDecls modl))
      <> emitAssetHeadArtifacts modl
      <> emitPageFlowArtifacts (lowerPageFlows modl)
      <> emitSeededFixturesExport (lowerModuleTypeDecls modl) (lowerModuleRecordDecls modl) (lowerModuleRoutes modl)
      <> emitRoutesExport (lowerModuleTypeDecls modl) (lowerModuleRecordDecls modl) (lowerModuleRoutes modl)
      <> emitRouteClientsExport (lowerModuleRoutes modl)
      <> emitSchemaRegistryExport (lowerModuleCodecTypes modl) (lowerModuleTypeDecls modl) (lowerModuleRecordDecls modl)
      <> emitPlatformBridgesExport
      <> emitControlPlaneExports modl
      <> emitGeneratedBindingsExport

