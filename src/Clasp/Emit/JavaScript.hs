emitDecls :: Int -> [LowerDecl] -> (Int, [Text])
emitDecls counter decls =
  foldl emitOne (counter, []) decls
  where
    emitOne (nextCounter, rendered) decl =
      let (updatedCounter, renderedDecl) = emitDecl nextCounter decl
       in (updatedCounter, rendered <> [renderedDecl])

emitDecl :: Int -> LowerDecl -> (Int, Text)
emitDecl counter decl =
  case decl of
    LValueDecl name expr ->
      let (nextCounter, bodyText) = emitExpr counter expr
       in ( nextCounter
          , "export const "
              <> emitIdentifier name
              <> " = "
              <> bodyText
              <> ";"
          )
    LFunctionDecl name params body ->
      let (nextCounter, bodyText) = emitExpr counter body
       in ( nextCounter
          , "export function "
              <> emitIdentifier name
              <> "("
              <> T.intercalate ", " (fmap emitIdentifier params)
              <> ") { return "
              <> bodyText
              <> "; }"
          )

emitExpr :: Int -> LowerExpr -> (Int, Text)
emitExpr counter expr =
  case expr of
    LVar name ->
      (counter, emitIdentifier name)
    LInt value ->
      (counter, T.pack (show value))
    LString value ->
      (counter, emitStringLiteral value)
    LBool True ->
      (counter, "true")
    LBool False ->
      (counter, "false")
    LPage title body ->
      let (counterAfterTitle, titleText) = emitExpr counter title
          (counterAfterBody, bodyText) = emitExpr counterAfterTitle body
       in ( counterAfterBody
          , "{ $kind: \"page\", title: " <> titleText <> ", body: " <> bodyText <> " }"
          )
    LRedirect targetPath ->
      (counter, "{ $kind: \"redirect\", location: " <> emitStringLiteral targetPath <> ", status: 303 }")
    LViewEmpty ->
      (counter, "{ $kind: \"empty\" }")
    LViewText value ->
      let (nextCounter, valueText) = emitExpr counter value
       in (nextCounter, "{ $kind: \"text\", text: " <> valueText <> " }")
    LViewAppend left right ->
      let (counterAfterLeft, leftText) = emitExpr counter left
          (counterAfterRight, rightText) = emitExpr counterAfterLeft right
       in ( counterAfterRight
          , "{ $kind: \"append\", left: " <> leftText <> ", right: " <> rightText <> " }"
          )
    LViewElement tagName child ->
      let (nextCounter, childText) = emitExpr counter child
       in ( nextCounter
          , "{ $kind: \"element\", tag: " <> emitStringLiteral tagName <> ", child: " <> childText <> " }"
          )
    LViewStyled styleRef child ->
      let (nextCounter, childText) = emitExpr counter child
       in ( nextCounter
          , "{ $kind: \"styled\", styleRef: " <> emitStringLiteral styleRef <> ", child: " <> childText <> " }"
          )
    LViewLink routeContract href child ->
      let (nextCounter, childText) = emitExpr counter child
       in ( nextCounter
          , "{ $kind: \"link\", contract: " <> emitRouteContractObject routeContract <> ", href: " <> emitStringLiteral href <> ", child: " <> childText <> " }"
          )
    LViewForm routeContract method action child ->
      let (nextCounter, childText) = emitExpr counter child
       in ( nextCounter
          , "{ $kind: \"form\", contract: " <> emitRouteContractObject routeContract <> ", method: " <> emitStringLiteral method <> ", action: " <> emitStringLiteral action <> ", child: " <> childText <> " }"
          )
    LViewInput fieldName inputKind value ->
      let (nextCounter, valueText) = emitExpr counter value
       in ( nextCounter
          , "{ $kind: \"input\", fieldName: " <> emitStringLiteral fieldName <> ", inputKind: " <> emitStringLiteral inputKind <> ", value: " <> valueText <> " }"
          )
    LViewSubmit label ->
      let (nextCounter, labelText) = emitExpr counter label
       in (nextCounter, "{ $kind: \"submit\", label: " <> labelText <> " }")
    LCall fn args ->
      let (counterAfterFn, fnText) = emitExpr counter fn
          (counterAfterArgs, argTexts) = emitExprList counterAfterFn args
       in ( counterAfterArgs
          , fnText
              <> "("
              <> T.intercalate ", " argTexts
              <> ")"
          )
    LConstruct tag fields ->
      let (nextCounter, fieldTexts) = emitExprList counter fields
          objectFields =
            T.intercalate ", " $
              ("$tag: " <> emitStringLiteral tag)
                : [ emitFieldName index <> ": " <> fieldText
                  | (index, fieldText) <- zip [(0 :: Int) ..] fieldTexts
                  ]
       in (nextCounter, "{ " <> objectFields <> " }")
    LRecord fields ->
      let (nextCounter, fieldTexts) = emitRecordFields counter fields
       in (nextCounter, "{ " <> T.intercalate ", " fieldTexts <> " }")
    LFieldAccess subject fieldName ->
      let (nextCounter, subjectText) = emitExpr counter subject
       in (nextCounter, "(" <> subjectText <> ")." <> emitPropertyName fieldName)
    LMatch subject branches ->
      let (counterAfterSubject, subjectText) = emitExpr counter subject
          matchVar = "$match" <> T.pack (show counterAfterSubject)
          (counterAfterBranches, branchTexts) = emitMatchBranches (counterAfterSubject + 1) matchVar branches
          switchBody =
            T.unlines $
              fmap ("    " <>) branchTexts
                <> ["    default: throw new Error(\"Non-exhaustive match\");"]
       in ( counterAfterBranches
          , T.unlines
              [ "(() => {"
              , "  const " <> matchVar <> " = " <> subjectText <> ";"
              , "  switch (" <> matchVar <> ".$tag) {"
              , switchBody
              , "  }"
              , "})()"
              ]
          )

emitRouteContractObject :: LowerRouteContract -> Text
emitRouteContractObject routeContract =
  "{ routeName: "
    <> emitStringLiteral (lowerRouteContractName routeContract)
    <> ", routeId: "
    <> emitStringLiteral (lowerRouteContractIdentity routeContract)
    <> ", method: "
    <> emitStringLiteral (lowerRouteContractMethod routeContract)
    <> ", path: "
    <> emitStringLiteral (lowerRouteContractPath routeContract)
    <> ", pathDecl: "
    <> emitRoutePathDecl (lowerRouteContractPathDecl routeContract)
    <> ", requestType: "
    <> emitStringLiteral (lowerRouteContractRequestType routeContract)
    <> ", queryDecl: "
    <> emitRouteBoundaryDecl [] [] (lowerRouteContractQueryDecl routeContract)
    <> ", formDecl: "
    <> emitRouteBoundaryDecl [] [] (lowerRouteContractFormDecl routeContract)
    <> ", bodyDecl: "
    <> emitRouteBoundaryDecl [] [] (lowerRouteContractBodyDecl routeContract)
    <> ", responseType: "
    <> emitStringLiteral (lowerRouteContractResponseType routeContract)
    <> ", responseDecl: "
    <> emitRouteBoundaryDecl [] [] (Just (lowerRouteContractResponseDecl routeContract))
    <> ", responseKind: "
    <> emitStringLiteral (lowerRouteContractResponseKind routeContract)
    <> " }"

emitExprList :: Int -> [LowerExpr] -> (Int, [Text])
emitExprList counter exprs =
  foldl emitOne (counter, []) exprs
  where
    emitOne (nextCounter, rendered) expr =
      let (updatedCounter, renderedExpr) = emitExpr nextCounter expr
       in (updatedCounter, rendered <> [renderedExpr])

emitRecordFields :: Int -> [LowerRecordField] -> (Int, [Text])
emitRecordFields counter fields =
  foldl emitOne (counter, []) fields
  where
    emitOne (nextCounter, rendered) field =
      let (updatedCounter, fieldValueText) = emitExpr nextCounter (lowerRecordFieldValue field)
          renderedField =
            emitPropertyName (lowerRecordFieldName field)
              <> ": "
              <> fieldValueText
       in (updatedCounter, rendered <> [renderedField])

emitMatchBranches :: Int -> Text -> [LowerMatchBranch] -> (Int, [Text])
emitMatchBranches counter matchVar branches =
  foldl emitOne (counter, []) branches
  where
    emitOne (nextCounter, rendered) branch =
      let (updatedCounter, renderedBranch) = emitMatchBranch nextCounter matchVar branch
       in (updatedCounter, rendered <> [renderedBranch])

emitMatchBranch :: Int -> Text -> LowerMatchBranch -> (Int, Text)
emitMatchBranch counter matchVar branch =
  let bindingLines =
        [ "const "
            <> emitIdentifier binder
            <> " = "
            <> matchVar
            <> "."
            <> emitFieldName index
            <> ";"
        | (index, binder) <- zip [(0 :: Int) ..] (lowerMatchBranchBinders branch)
        ]
      (nextCounter, bodyText) = emitExpr counter (lowerMatchBranchBody branch)
      bodyLines =
        fmap ("      " <>) bindingLines
          <> ["      return " <> bodyText <> ";"]
   in ( nextCounter
      , T.unlines $
          [ "case " <> emitStringLiteral (lowerMatchBranchTag branch) <> ": {"
          ]
            <> bodyLines
            <> ["    }"]
      )

emitFieldName :: Int -> Text
emitFieldName index =
  "$" <> T.pack (show index)

emitPropertyName :: Text -> Text
emitPropertyName = emitIdentifier

emitIdentifier :: Text -> Text
emitIdentifier = id

emitStringLiteral :: Text -> Text
emitStringLiteral value =
  T.pack (show (T.unpack value))
