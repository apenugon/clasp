emitModule :: LowerModule -> Text
emitModule modl =
  T.unlines $
    [ "// Generated by claspc"
    , "// Source module: " <> unModuleName (lowerModuleName modl)
    ]
      <> emitRuntimePrelude
      <> emitPrimitiveCodecHelpers
      <> emitRequestSchemaHelpers (lowerModuleTypeDecls modl) (lowerModuleRecordDecls modl)
      <> concatMap emitTypeCodecHelpers (lowerModuleTypeDecls modl)
      <> concatMap emitRecordCodecHelpers (lowerModuleRecordDecls modl)
      <> emitForeignBindings (lowerModuleTypeDecls modl) (lowerModuleRecordDecls modl) (lowerModuleForeignDecls modl)
      <> snd (emitDecls 0 (lowerModuleDecls modl))
      <> emitPageFlowArtifacts (lowerPageFlows modl)
      <> emitRoutesExport (lowerModuleTypeDecls modl) (lowerModuleRecordDecls modl) (lowerModuleRoutes modl)

emitRuntimePrelude :: [Text]
emitRuntimePrelude =
  [ ""
  , "function $claspRuntime(name) {"
  , "  const runtime = globalThis.__claspRuntime;"
  , "  if (!runtime || !(name in runtime)) {"
  , "    throw new Error(`Missing Clasp runtime binding: ${name}`);"
  , "  }"
  , "  return runtime[name];"
  , "}"
  , ""
  , "function $claspHostBinding(name) {"
  , "  const binding = $claspHostBindingMap[name];"
  , "  if (!binding) {"
  , "    throw new Error(`Missing Clasp host binding manifest: ${name}`);"
  , "  }"
  , "  return binding;"
  , "}"
  , ""
  , "function $claspCallHostBinding(name, args) {"
  , "  const binding = $claspHostBinding(name);"
  , "  const runtime = $claspRuntime(binding.runtimeName);"
  , "  const hostArgs = binding.params.map((param, index) => param.toHost(args[index]));"
  , "  return binding.returns.fromHost(runtime(...hostArgs));"
  , "}"
  , ""
  , "function $claspExpectObject(value, path) {"
  , "  if (typeof value !== \"object\" || value === null || Array.isArray(value)) {"
  , "    throw new Error(`${path} expected an object`);"
  , "  }"
  , "  return value;"
  , "}"
  , ""
  , "function $claspBoundaryLabel(path) {"
  , "  const index = path.lastIndexOf(\".\");"
  , "  return index >= 0 ? path.slice(index + 1) : path;"
  , "}"
  , ""
  , "function $claspExpectInt(value, path) {"
  , "  if (!Number.isInteger(value)) {"
  , "    throw new Error(`${$claspBoundaryLabel(path)} must be an integer`);"
  , "  }"
  , "  return value;"
  , "}"
  , ""
  , "function $claspExpectStr(value, path) {"
  , "  if (typeof value !== \"string\") {"
  , "    throw new Error(`${$claspBoundaryLabel(path)} must be a string`);"
  , "  }"
  , "  return value;"
  , "}"
  , ""
  , "function $claspExpectBool(value, path) {"
  , "  if (typeof value !== \"boolean\") {"
  , "    throw new Error(`${$claspBoundaryLabel(path)} must be a boolean`);"
  , "  }"
  , "  return value;"
  , "}"
  , ""
  , "function $claspRequireField(objectValue, fieldName, path) {"
  , "  if (!Object.prototype.hasOwnProperty.call(objectValue, fieldName)) {"
  , "    throw new Error(`${path}.${fieldName} is missing`);"
  , "  }"
  , "  return objectValue[fieldName];"
  , "}"
  , ""
  , "function $claspEscapeHtml(text) {"
  , "  return text"
  , "    .replaceAll(\"&\", \"&amp;\")"
  , "    .replaceAll(\"<\", \"&lt;\")"
  , "    .replaceAll(\">\", \"&gt;\")"
  , "    .replaceAll('\"', \"&quot;\")"
  , "    .replaceAll(\"'\", \"&#39;\");"
  , "}"
  , ""
  , "function $claspExpectView(value, path) {"
  , "  const objectValue = $claspExpectObject(value, path);"
  , "  const kind = $claspRequireField(objectValue, \"$kind\", path);"
  , "  switch (kind) {"
  , "    case \"empty\": return objectValue;"
  , "    case \"text\":"
  , "      $claspExpectStr($claspRequireField(objectValue, \"text\", path), `${path}.text`);"
  , "      return objectValue;"
  , "    case \"append\":"
  , "      $claspExpectView($claspRequireField(objectValue, \"left\", path), `${path}.left`);"
  , "      $claspExpectView($claspRequireField(objectValue, \"right\", path), `${path}.right`);"
  , "      return objectValue;"
  , "    case \"element\":"
  , "      $claspExpectStr($claspRequireField(objectValue, \"tag\", path), `${path}.tag`);"
  , "      $claspExpectView($claspRequireField(objectValue, \"child\", path), `${path}.child`);"
  , "      return objectValue;"
  , "    case \"styled\":"
  , "      $claspExpectStr($claspRequireField(objectValue, \"styleRef\", path), `${path}.styleRef`);"
  , "      $claspExpectView($claspRequireField(objectValue, \"child\", path), `${path}.child`);"
  , "      return objectValue;"
  , "    case \"link\":"
  , "      $claspExpectStr($claspRequireField(objectValue, \"href\", path), `${path}.href`);"
  , "      $claspExpectView($claspRequireField(objectValue, \"child\", path), `${path}.child`);"
  , "      return objectValue;"
  , "    case \"form\":"
  , "      $claspExpectStr($claspRequireField(objectValue, \"method\", path), `${path}.method`);"
  , "      $claspExpectStr($claspRequireField(objectValue, \"action\", path), `${path}.action`);"
  , "      $claspExpectView($claspRequireField(objectValue, \"child\", path), `${path}.child`);"
  , "      return objectValue;"
  , "    case \"input\":"
  , "      $claspExpectStr($claspRequireField(objectValue, \"fieldName\", path), `${path}.fieldName`);"
  , "      $claspExpectStr($claspRequireField(objectValue, \"inputKind\", path), `${path}.inputKind`);"
  , "      $claspExpectStr($claspRequireField(objectValue, \"value\", path), `${path}.value`);"
  , "      return objectValue;"
  , "    case \"submit\":"
  , "      $claspExpectStr($claspRequireField(objectValue, \"label\", path), `${path}.label`);"
  , "      return objectValue;"
  , "    default:"
  , "      throw new Error(`${path} expected a View value`);"
  , "  }"
  , "}"
  , ""
  , "function $claspExpectPage(value, path = \"value\") {"
  , "  const objectValue = $claspExpectObject(value, path);"
  , "  if ($claspRequireField(objectValue, \"$kind\", path) !== \"page\") {"
  , "    throw new Error(`${path} expected a Page value`);"
  , "  }"
  , "  $claspExpectStr($claspRequireField(objectValue, \"title\", path), `${path}.title`);"
  , "  $claspExpectView($claspRequireField(objectValue, \"body\", path), `${path}.body`);"
  , "  return objectValue;"
  , "}"
  , ""
  , "function $claspExpectRedirect(value, path = \"value\") {"
  , "  const objectValue = $claspExpectObject(value, path);"
  , "  if ($claspRequireField(objectValue, \"$kind\", path) !== \"redirect\") {"
  , "    throw new Error(`${path} expected a Redirect value`);"
  , "  }"
  , "  const location = $claspExpectStr($claspRequireField(objectValue, \"location\", path), `${path}.location`);"
  , "  const status = $claspExpectInt(objectValue.status ?? 303, `${path}.status`);"
  , "  return { $kind: \"redirect\", location, status };"
  , "}"
  , ""
  , "function $prepare_Redirect(value) {"
  , "  return $claspExpectRedirect(value, \"value\");"
  , "}"
  , ""
  , "function $claspRenderFlowAttrs(contract, flowKind) {"
  , "  if (!contract || typeof contract !== \"object\") {"
  , "    return ` data-clasp-flow=\"${$claspEscapeHtml(flowKind)}\"`;"
  , "  }"
  , "  const routeName = typeof contract.routeName === \"string\" ? contract.routeName : \"\";"
  , "  const routeId = typeof contract.routeId === \"string\" ? contract.routeId : \"\";"
  , "  const routeMethod = typeof contract.method === \"string\" ? contract.method : \"\";"
  , "  const routePath = typeof contract.path === \"string\" ? contract.path : \"\";"
  , "  const requestType = typeof contract.requestType === \"string\" ? contract.requestType : \"\";"
  , "  const responseType = typeof contract.responseType === \"string\" ? contract.responseType : \"\";"
  , "  const responseKind = typeof contract.responseKind === \"string\" ? contract.responseKind : \"\";"
  , "  return ` data-clasp-flow=\"${$claspEscapeHtml(flowKind)}\" data-clasp-route=\"${$claspEscapeHtml(routeName)}\" data-clasp-route-id=\"${$claspEscapeHtml(routeId)}\" data-clasp-method=\"${$claspEscapeHtml(routeMethod)}\" data-clasp-path=\"${$claspEscapeHtml(routePath)}\" data-clasp-request-type=\"${$claspEscapeHtml(requestType)}\" data-clasp-response-type=\"${$claspEscapeHtml(responseType)}\" data-clasp-response-kind=\"${$claspEscapeHtml(responseKind)}\"`;"
  , "}"
  , ""
  , "function $claspRenderView(view) {"
  , "  const checkedView = $claspExpectView(view, \"view\");"
  , "  switch (checkedView.$kind) {"
  , "    case \"empty\":"
  , "      return \"\";"
  , "    case \"text\":"
  , "      return $claspEscapeHtml(checkedView.text);"
  , "    case \"append\":"
  , "      return $claspRenderView(checkedView.left) + $claspRenderView(checkedView.right);"
  , "    case \"element\":"
  , "      return `<${checkedView.tag}>${$claspRenderView(checkedView.child)}</${checkedView.tag}>`;"
  , "    case \"styled\":"
  , "      return `<div data-clasp-style=${JSON.stringify($claspExpectStr(checkedView.styleRef, \"view.styleRef\"))}>${$claspRenderView(checkedView.child)}</div>`;"
  , "    case \"link\":"
  , "      return `<a${$claspRenderFlowAttrs(checkedView.contract, \"navigation\")} href=\"${$claspEscapeHtml($claspExpectStr(checkedView.href, \"view.href\"))}\">${$claspRenderView(checkedView.child)}</a>`;"
  , "    case \"form\":"
  , "      return `<form${$claspRenderFlowAttrs(checkedView.contract, \"action\")} method=\"${$claspEscapeHtml($claspExpectStr(checkedView.method, \"view.method\"))}\" action=\"${$claspEscapeHtml($claspExpectStr(checkedView.action, \"view.action\"))}\">${$claspRenderView(checkedView.child)}</form>`;"
  , "    case \"input\":"
  , "      return `<input name=\"${$claspEscapeHtml($claspExpectStr(checkedView.fieldName, \"view.fieldName\"))}\" type=\"${$claspEscapeHtml($claspExpectStr(checkedView.inputKind, \"view.inputKind\"))}\" value=\"${$claspEscapeHtml($claspExpectStr(checkedView.value, \"view.value\"))}\">`;"
  , "    case \"submit\":"
  , "      return `<button type=\"submit\">${$claspEscapeHtml($claspExpectStr(checkedView.label, \"view.label\"))}</button>`;"
  , "    default:"
  , "      throw new Error(\"Unknown View node\");"
  , "  }"
  , "}"
  , ""
  , "function $render_Page(value) {"
  , "  const page = $claspExpectPage(value);"
  , "  return \"<!DOCTYPE html><html><head><meta charset=\\\"utf-8\\\"><title>\" + $claspEscapeHtml(page.title) + \"</title></head><body>\" + $claspRenderView(page.body) + \"</body></html>\";"
  , "}"
  , ""
  ]

