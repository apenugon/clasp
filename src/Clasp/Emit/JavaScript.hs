renderControlPlaneDocsMarkdown :: LowerModule -> Text
renderControlPlaneDocsMarkdown modl =
  T.unlines $
      [ "# Control Plane Docs"
    , ""
    , "This document is generated from the same Clasp declarations as the executable control-plane manifests."
    , ""
    , "## Module"
    , ""
    , "- Name: " <> unModuleName (lowerModuleName modl)
    , "- Version id: " <> moduleVersionIdentifier modl
    , "- Upgrade window: bounded-overlap"
    ]
      <> renderDocsSection "Workflows" (fmap renderWorkflowDoc (lowerModuleWorkflowDecls modl))
      <> renderDocsSection "Supervisors" (fmap renderSupervisorDoc (lowerModuleSupervisorDecls modl))
      <> renderDocsSection "Guides" (fmap renderGuideDoc (lowerModuleGuideDecls modl))
      <> renderDocsSection "Policies" (fmap renderPolicyDoc (lowerModulePolicyDecls modl))
      <> renderDocsSection "Secret Inputs" (renderSecretInputDocs (lowerModulePolicyDecls modl) (lowerModuleAgentRoleDecls modl) (lowerModuleToolServerDecls modl))
      <> renderDocsSection "Agent Roles" (fmap renderAgentRoleDoc (lowerModuleAgentRoleDecls modl))
      <> renderDocsSection "Agents" (fmap renderAgentDoc (lowerModuleAgentDecls modl))
      <> renderDocsSection "Hooks" (fmap renderHookDoc (lowerModuleHookDecls modl))
      <> renderDocsSection "Tool Servers" (fmap renderToolServerDoc (lowerModuleToolServerDecls modl))
      <> renderDocsSection "Tools" (fmap renderToolDoc (lowerModuleToolDecls modl))
      <> renderDocsSection "Verifiers" (fmap renderVerifierDoc (lowerModuleVerifierDecls modl))
      <> renderDocsSection "Merge Gates" (fmap renderMergeGateDoc (lowerModuleMergeGateDecls modl))

renderDocsSection :: Text -> [Text] -> [Text]
renderDocsSection title entries =
  [ ""
  , "## " <> title
  ]
    <> case entries of
      [] ->
        [ ""
        , "_None declared._"
        ]
      _ ->
        [ ""
        , T.intercalate "\n\n" entries
        ]

renderWorkflowDoc :: WorkflowDecl -> Text
renderWorkflowDoc workflowDecl =
  T.unlines
    [ "### " <> workflowDeclName workflowDecl
    , "- State schema: " <> renderType (workflowDeclStateType workflowDecl)
    , "- Compatibility metadata: checkpoint schema `" <> renderType (workflowDeclStateType workflowDecl) <> "` with bounded-overlap module upgrades"
    , "- Checkpoint: `workflow.checkpoint(state) -> Str`"
    , "- Resume: `workflow.resume(snapshot) -> " <> renderType (workflowDeclStateType workflowDecl) <> "`"
    , "- Start: `workflow.start(snapshot, { deadlineAt?, retry?, supervisor?, mailbox? }) -> { checkpoint, state, deliveries, processedIds, mailbox, deadlineAt, cancelled, retryPolicy, supervision, auditLog }`"
    , "- Temporal: `workflow.temporal.clock(seedNow?)` creates a simulated clock and `workflow.temporal.{deadline,ttl,expiration,schedule,rollout,cache,capability}(spec, { now?, clock? })` evaluates first-class temporal semantics"
    , "- Lifecycle: `workflow.withDeadline(run, deadlineAt)` updates the run deadline and `workflow.cancel(run, reason?)` marks a run as cancelled"
    , "- Supervision: `workflow.degrade(run, reason, { supervisor?, updatedAt? })` moves a run into supervised degraded mode and `workflow.handoff(run, operator, reason, { supervisor?, updatedAt? })` suspends automation for an operator"
    , "- Mailbox: `workflow.enqueue(run, { id, payload })` appends unique pending messages, `workflow.processNext(run, reducer, options?)` consumes one queued message, and `workflow.drainMailbox(run, reducer, options?)` drains until the mailbox is empty or supervision blocks delivery"
    , "- Deliver: `workflow.deliver(run, { id, payload }, reducer, { now?, clock?, retry?, degradedHandler? })` suppresses duplicates, enforces cancellation and deadlines, retries reducer failures with bounded backoff, and allows supervised degraded-mode fallbacks"
    , "- Audit: workflow runs accumulate typed `auditLog` entries for transitions, retries, operator handoffs, and upgrades; delivery results surface the emitted entries in `audit`"
    , "- Replay: `workflow.replay(snapshot, messages, reducer, options?)` deterministically rebuilds message-driven state and a fresh replay audit log until cancellation, deadline, or retry exhaustion"
    , "- Migrate: `workflow.migrate(snapshot, targetWorkflow?, { migrateState? })` applies an explicit state migration hook at a hot-swap boundary before re-checkpointing against the target workflow"
    , "- Upgrade: `workflow.upgrade(run, targetWorkflow?, { migrateState?, prepare?, activate? })` preserves durable run metadata while requiring explicit upgrade handlers for supervised hot swaps"
    ]

renderSupervisorDoc :: SupervisorDecl -> Text
renderSupervisorDoc supervisorDecl =
  T.unlines
    [ "### " <> supervisorDeclName supervisorDecl
    , "- Restart strategy: `" <> renderSupervisorRestartStrategy (supervisorDeclRestartStrategy supervisorDecl) <> "`"
    , "- Children: " <> renderSupervisorChildrenDoc (supervisorDeclChildren supervisorDecl)
    ]

renderSupervisorChildrenDoc :: [SupervisorChildDecl] -> Text
renderSupervisorChildrenDoc childDecls =
  case childDecls of
    [] ->
      "_None declared._"
    _ ->
      T.intercalate ", " (fmap renderSupervisorChildDoc childDecls)

renderSupervisorChildDoc :: SupervisorChildDecl -> Text
renderSupervisorChildDoc childDecl =
  case childDecl of
    SupervisorWorkflowChild {supervisorChildName = childName} ->
      "`workflow " <> childName <> "`"
    SupervisorSupervisorChild {supervisorChildName = childName} ->
      "`supervisor " <> childName <> "`"

renderGuideDoc :: GuideDecl -> Text
