  , ""
  ]

emitRequestSchemaHelpers :: [TypeDecl] -> [RecordDecl] -> [Text]
emitRequestSchemaHelpers typeDecls recordDecls =
  [ "const $claspSchema_Int = { kind: \"int\" };"
  , "const $claspSchema_Str = { kind: \"str\" };"
  , "const $claspSchema_Bool = { kind: \"bool\" };"
  ]
    <> concatMap emitTypeSchema typeDecls
    <> concatMap emitRecordSchema recordDecls
    <> [""]
  where
    emitTypeSchema typeDecl
      | isJsonEnumTypeDecl typeDecl =
          [ "const $claspSchema_" <> typeDeclName typeDecl <> " = { kind: \"enum\", name: " <> emitStringLiteral (typeDeclName typeDecl) <> " };"
          ]
      | otherwise =
          []

    emitRecordSchema recordDecl =
      [ "const $claspSchema_" <> recordDeclName recordDecl <> " = {"
      , "  kind: \"record\","
      , "  name: " <> emitStringLiteral (recordDeclName recordDecl) <> ","
      , "  classificationPolicy: " <> emitOptionalText (recordDeclProjectionPolicy recordDecl) <> ","
      , "  projectionSource: " <> emitOptionalText (recordDeclProjectionSource recordDecl) <> ","
      , "  fields: {"
      ]
        <> fmap emitRecordFieldSchema (recordDeclFields recordDecl)
        <> [ "  }"
           , "};"
           ]

    emitRecordFieldSchema fieldDecl =
      "    "
        <> recordFieldDeclName fieldDecl
        <> ": {"
        <> " schema: "
        <> emitRequestSchemaRef (recordFieldDeclType fieldDecl)
        <> ", classification: "
        <> emitStringLiteral (recordFieldDeclClassification fieldDecl)
        <> " },"

    emitRequestSchemaRef typ =
      case typ of
        TInt ->
          "$claspSchema_Int"
        TStr ->
          "$claspSchema_Str"
        TBool ->
          "$claspSchema_Bool"
        TNamed name ->
          if any ((== name) . typeDeclName) typeDecls || any ((== name) . recordDeclName) recordDecls
            then "$claspSchema_" <> name
            else "null"
        TFunction _ _ ->
          "null"

emitOptionalText :: Maybe Text -> Text
emitOptionalText maybeText =
  case maybeText of
    Just value ->
      emitStringLiteral value
    Nothing ->
      "null"

emitTypeCodecHelpers :: TypeDecl -> [Text]
emitTypeCodecHelpers typeDecl
  | isJsonEnumTypeDecl typeDecl =
      [ "function " <> validateName <> "(value, path = \"value\") {"
      , "  switch (value) {"
      ]
        <> concatMap emitValidateCase constructors
        <> [ "    default: throw new Error(`${$claspBoundaryLabel(path)} must be one of: " <> expectedValues <> "`);"
           , "  }"
           , "}"
           , "function " <> validateInternalName <> "(value, path = \"value\") {"
           , "  const tag = typeof value === \"object\" && value !== null ? value.$tag : null;"
           , "  switch (tag) {"
           ]
        <> concatMap emitValidateInternalCase constructors
        <> [ "    default: throw new Error(`${path} expected a " <> typeDeclName typeDecl <> " value`);"
           , "  }"
           , "}"
           , "function " <> serializeName <> "(value, path = \"value\") {"
           , "  const tag = typeof value === \"object\" && value !== null ? value.$tag : null;"
           , "  switch (tag) {"
           ]
        <> concatMap emitSerializeCase constructors
        <> [ "    default: throw new Error(`${path} expected a " <> typeDeclName typeDecl <> " value`);"
           , "  }"
           , "}"
           , "function " <> decodeName <> "(jsonText) { return " <> validateName <> "(JSON.parse(jsonText), \"value\"); }"
           , "function " <> encodeName <> "(value) { return JSON.stringify(" <> serializeName <> "(value, \"value\")); }"
           , ""
           ]
  | otherwise =
      []
  where
    constructors = typeDeclConstructors typeDecl
    expectedValues = T.intercalate ", " (fmap constructorWireValue constructors)
    validateName = "$validate_" <> typeDeclName typeDecl
    validateInternalName = "$validateInternal_" <> typeDeclName typeDecl
    serializeName = "$serialize_" <> typeDeclName typeDecl
    decodeName = "$decode_" <> typeDeclName typeDecl
    encodeName = "$encode_" <> typeDeclName typeDecl

    emitValidateCase constructorDecl =
      [ "    case "
          <> emitStringLiteral (constructorWireValue constructorDecl)
          <> ": return "
          <> emitIdentifier (constructorDeclName constructorDecl)
          <> ";"
      ]

    emitValidateInternalCase constructorDecl =
      [ "    case "
          <> emitStringLiteral (constructorDeclName constructorDecl)
          <> ": return value;"
      ]

    emitSerializeCase constructorDecl =
      [ "    case "
          <> emitStringLiteral (constructorDeclName constructorDecl)
          <> ": return "
          <> emitStringLiteral (constructorWireValue constructorDecl)
          <> ";"
      ]

emitRecordCodecHelpers :: RecordDecl -> [Text]
emitRecordCodecHelpers recordDecl =
  [ "function " <> validateName <> "(value, path = \"value\") {"
  , "  const objectValue = $claspExpectObject(value, path);"
  ]
    <> concatMap emitValidateField (recordDeclFields recordDecl)
    <> [ "  return { " <> T.intercalate ", " (fmap (\fieldDecl -> recordFieldDeclName fieldDecl <> ": " <> recordFieldDeclName fieldDecl) (recordDeclFields recordDecl)) <> " };"
       , "}"
       , "function " <> validateInternalName <> "(value, path = \"value\") {"
       , "  const objectValue = $claspExpectObject(value, path);"
       ]
    <> concatMap emitValidateInternalField (recordDeclFields recordDecl)
    <> [ "  return { " <> T.intercalate ", " (fmap (\fieldDecl -> recordFieldDeclName fieldDecl <> ": " <> recordFieldDeclName fieldDecl) (recordDeclFields recordDecl)) <> " };"
       , "}"
       , "function " <> serializeName <> "(value) {"
       ]
    <> concatMap emitSerializeField (recordDeclFields recordDecl)
    <> [ "  return { " <> T.intercalate ", " (fmap (\fieldDecl -> recordFieldDeclName fieldDecl <> ": " <> recordFieldDeclName fieldDecl) (recordDeclFields recordDecl)) <> " };"
       , "}"
       , "function " <> decodeName <> "(jsonText) { return " <> validateName <> "(JSON.parse(jsonText), \"value\"); }"
       , "function " <> encodeName <> "(value) { return JSON.stringify(" <> serializeName <> "(" <> validateInternalName <> "(value, \"value\"))); }"
       , ""
       ]
  where
    validateName = "$validate_" <> recordDeclName recordDecl
    validateInternalName = "$validateInternal_" <> recordDeclName recordDecl
    serializeName = "$serialize_" <> recordDeclName recordDecl
    decodeName = "$decode_" <> recordDeclName recordDecl
    encodeName = "$encode_" <> recordDeclName recordDecl

    emitValidateField fieldDecl =
      let fieldName = recordFieldDeclName fieldDecl
          fieldPath = "path + " <> emitStringLiteral ("." <> fieldName)
       in [ "  const " <> fieldName <> "Value = objectValue[" <> emitStringLiteral fieldName <> "];"
          , "  const " <> fieldName <> " = " <> emitValidator (recordFieldDeclType fieldDecl) (fieldName <> "Value") fieldPath <> ";"
          ]

    emitValidateInternalField fieldDecl =
      let fieldName = recordFieldDeclName fieldDecl
          fieldPath = "path + " <> emitStringLiteral ("." <> fieldName)
       in [ "  const " <> fieldName <> "Value = $claspRequireField(objectValue, " <> emitStringLiteral fieldName <> ", path);"
          , "  const " <> fieldName <> " = " <> emitInternalValidator (recordFieldDeclType fieldDecl) (fieldName <> "Value") fieldPath <> ";"
          ]

    emitSerializeField fieldDecl =
      let fieldName = recordFieldDeclName fieldDecl
       in [ "  const " <> fieldName <> " = " <> emitSerializer (recordFieldDeclType fieldDecl) ("value." <> fieldName) <> ";" ]

emitForeignBindings :: [TypeDecl] -> [RecordDecl] -> [ForeignDecl] -> [Text]
emitForeignBindings typeDecls recordDecls foreignDecls =
  emitHostBindingManifest
    <> fmap emitForeignBinding foreignDecls
    <> [""]
  where
    emitHostBindingManifest
      | null foreignDecls =
          [ "export const __claspHostBindings = [];"
          , "const $claspHostBindingMap = {};"
          ]
      | otherwise =
          [ "export const __claspHostBindings = ["
          ]
            <> concatMap emitHostBindingEntry foreignDecls
            <> [ "];"
               , "const $claspHostBindingMap = Object.fromEntries(__claspHostBindings.map((binding) => [binding.name, binding]));"
               ]

    emitHostBindingEntry foreignDecl =
      let (paramTypes, returnType) = splitFunctionType (foreignDeclType foreignDecl)
       in [ "  {"
          , "    name: " <> emitStringLiteral (foreignDeclName foreignDecl) <> ","
          , "    runtimeName: " <> emitStringLiteral (foreignDeclRuntimeName foreignDecl) <> ","
          , "    params: ["
          ]
            <> concat
              [ [ "      {"
                , "        index: " <> T.pack (show index) <> ","
                , "        type: " <> emitStringLiteral (renderTypeName paramType) <> ","
                , "        schema: " <> emitSchemaRef typeDecls recordDecls paramType <> ","
                , "        toHost(value) { return " <> emitHostSerializer paramType "value" "\"value\"" <> "; }"
                , "      },"
                ]
              | (index, paramType) <- zip [(0 :: Int) ..] paramTypes
              ]
            <> [ "    ],"
               , "    returns: {"
               , "      type: " <> emitStringLiteral (renderTypeName returnType) <> ","
               , "      schema: " <> emitSchemaRef typeDecls recordDecls returnType <> ","
               , "      fromHost(value) { return " <> emitHostDeserializer returnType "value" "\"result\"" <> "; }"
               , "    }"
               , "  },"
               ]

    emitForeignBinding foreignDecl =
      let params = foreignParams (foreignDeclType foreignDecl)
       in "function "
            <> emitIdentifier (foreignDeclName foreignDecl)
            <> "("
            <> T.intercalate ", " params
            <> ") { return $claspCallHostBinding("
            <> emitStringLiteral (foreignDeclName foreignDecl)
            <> ", ["
            <> T.intercalate ", " params
            <> "]); }"

emitPageFlowArtifacts :: [LowerPageFlow] -> [Text]
emitPageFlowArtifacts pageFlows =
  [ "export const __claspUiGraph = ["
  ]
    <> concatMap emitUiPage pageFlows
    <> [ "];"
       , "export const __claspNavigationGraph = ["
       ]
    <> concatMap emitNavigationEdges pageFlows
    <> [ "];"
       , "export const __claspActionGraph = ["
       ]
    <> concatMap emitActionEdges pageFlows
    <> [ "];"
       ]
  where
    emitUiPage pageFlow =
      [ "  {"
      , "    routeName: " <> emitStringLiteral (lowerPageFlowRouteName pageFlow) <> ","
      , "    routeId: " <> emitStringLiteral (lowerPageFlowRouteIdentity pageFlow) <> ","
      , "    path: " <> emitStringLiteral (lowerPageFlowPath pageFlow) <> ","
      , "    handler: " <> emitStringLiteral (lowerPageFlowHandlerName pageFlow) <> ","
      , "    title: " <> emitStringLiteral (lowerPageFlowTitle pageFlow) <> ","
      , "    texts: [" <> T.intercalate ", " (fmap emitStringLiteral (lowerPageFlowTexts pageFlow)) <> "],"
      , "    links: [" <> T.intercalate ", " (fmap emitPageLink (lowerPageFlowLinks pageFlow)) <> "],"
      , "    forms: [" <> T.intercalate ", " (fmap emitPageForm (lowerPageFlowForms pageFlow)) <> "]"
      , "  },"
      ]

    emitNavigationEdges pageFlow =
      fmap (emitNavigationEdge pageFlow) (lowerPageFlowLinks pageFlow)

    emitNavigationEdge pageFlow pageLink =
      "  { sourceRoute: "
        <> emitStringLiteral (lowerPageFlowRouteName pageFlow)
        <> ", sourceRouteId: "
        <> emitStringLiteral (lowerPageFlowRouteIdentity pageFlow)
        <> ", sourcePath: "
        <> emitStringLiteral (lowerPageFlowPath pageFlow)
        <> ", targetRoute: "
        <> emitStringLiteral (lowerPageLinkRouteName pageLink)
        <> ", targetRouteId: "
        <> emitStringLiteral (lowerPageLinkRouteIdentity pageLink)
        <> ", targetPath: "
        <> emitStringLiteral (lowerPageLinkPath pageLink)
        <> ", href: "
        <> emitStringLiteral (lowerPageLinkHref pageLink)
        <> ", label: "
        <> emitStringLiteral (lowerPageLinkLabel pageLink)
        <> " },"

    emitActionEdges pageFlow =
      fmap (emitActionEdge pageFlow) (lowerPageFlowForms pageFlow)

    emitActionEdge pageFlow pageForm =
      "  { sourceRoute: "
        <> emitStringLiteral (lowerPageFlowRouteName pageFlow)
        <> ", sourceRouteId: "
        <> emitStringLiteral (lowerPageFlowRouteIdentity pageFlow)
        <> ", sourcePath: "
        <> emitStringLiteral (lowerPageFlowPath pageFlow)
        <> ", actionRoute: "
        <> emitStringLiteral (lowerPageFormRouteName pageForm)
        <> ", actionRouteId: "
        <> emitStringLiteral (lowerPageFormRouteIdentity pageForm)
        <> ", actionPath: "
        <> emitStringLiteral (lowerPageFormPath pageForm)
        <> ", method: "
        <> emitStringLiteral (lowerPageFormMethod pageForm)
        <> ", action: "
        <> emitStringLiteral (lowerPageFormAction pageForm)
        <> ", requestType: "
        <> emitStringLiteral (lowerPageFormRequestType pageForm)
        <> ", responseType: "
