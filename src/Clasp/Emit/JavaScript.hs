           , ""
           ]
  | otherwise =
      [ "function " <> validateName <> "(value, path = \"value\") {"
      , "  return " <> validateInternalName <> "(value, path);"
      , "}"
      , "function " <> validateInternalName <> "(value, path = \"value\") {"
      , "  const objectValue = $claspExpectObject(value, path);"
      , "  const tag = typeof objectValue.$tag === \"string\" ? objectValue.$tag : null;"
      , "  switch (tag) {"
      ]
        <> concatMap emitValidateInternalCase constructors
        <> [ "    default: throw new Error(`${path} expected a " <> typeDeclName typeDecl <> " value`);"
           , "  }"
           , "}"
           , "function " <> serializeName <> "(value, path = \"value\") {"
           , "  const objectValue = $claspExpectObject(value, path);"
           , "  const tag = typeof objectValue.$tag === \"string\" ? objectValue.$tag : null;"
           , "  switch (tag) {"
           ]
        <> concatMap emitSerializeCase constructors
        <> [ "    default: throw new Error(`${path} expected a " <> typeDeclName typeDecl <> " value`);"
           , "  }"
           , "}"
           , "function " <> decodeName <> "(jsonText) { return " <> validateName <> "(JSON.parse(jsonText), \"value\"); }"
           , "function " <> encodeName <> "(value) { return JSON.stringify(" <> serializeName <> "(value, \"value\")); }"
           , ""
           ]
  where
    constructors = typeDeclConstructors typeDecl
    expectedValues = T.intercalate ", " (fmap constructorWireValue constructors)
    validateName = "$validate_" <> typeDeclName typeDecl
    validateInternalName = "$validateInternal_" <> typeDeclName typeDecl
    serializeName = "$serialize_" <> typeDeclName typeDecl
    decodeName = "$decode_" <> typeDeclName typeDecl
    encodeName = "$encode_" <> typeDeclName typeDecl

    emitValidateCase constructorDecl =
      [ "    case "
          <> emitStringLiteral (constructorWireValue constructorDecl)
          <> ": return "
          <> emitIdentifier (constructorDeclName constructorDecl)
          <> ";"
      ]

    emitEnumValidateInternalCase constructorDecl =
      [ "    case "
          <> emitStringLiteral (constructorDeclName constructorDecl)
          <> ": return value;"
      ]

    emitEnumSerializeCase constructorDecl =
      [ "    case "
          <> emitStringLiteral (constructorDeclName constructorDecl)
          <> ": return "
          <> emitStringLiteral (constructorWireValue constructorDecl)
          <> ";"
      ]

    emitValidateInternalCase constructorDecl
      | null (constructorDeclFields constructorDecl) =
          [ "    case "
              <> emitStringLiteral (constructorDeclName constructorDecl)
              <> ": return objectValue;"
          ]
      | otherwise =
          [ "    case "
              <> emitStringLiteral (constructorDeclName constructorDecl)
              <> ": {"
          ]
            <> concatMap emitValidateInternalField (zip [(0 :: Int) ..] (constructorDeclFields constructorDecl))
            <> [ "      return { "
                    <> T.intercalate ", " ("$tag: objectValue.$tag" : fmap emitFieldReturn [0 .. length (constructorDeclFields constructorDecl) - 1])
                    <> " };"
               , "    }"
               ]

    emitValidateInternalField (index, fieldType) =
      let fieldName = emitFieldName index
          path = "`${path}." <> fieldName <> "`"
       in [ "      const "
              <> fieldName
              <> " = "
              <> emitHostDeserializer fieldType ("objectValue." <> fieldName) path
              <> ";"
          ]

    emitSerializeCase constructorDecl
      | null (constructorDeclFields constructorDecl) =
          [ "    case "
              <> emitStringLiteral (constructorDeclName constructorDecl)
              <> ": return "
              <> "{ $tag: objectValue.$tag };"
              <> ";"
          ]
      | otherwise =
          [ "    case "
              <> emitStringLiteral (constructorDeclName constructorDecl)
              <> ": {"
          ]
            <> concatMap emitSerializeField (zip [(0 :: Int) ..] (constructorDeclFields constructorDecl))
            <> [ "      return { "
                    <> T.intercalate ", " ("$tag: objectValue.$tag" : fmap emitFieldReturn [0 .. length (constructorDeclFields constructorDecl) - 1])
                    <> " };"
               , "    }"
               ]

    emitSerializeField (index, fieldType) =
      let fieldName = emitFieldName index
          path = "`${path}." <> fieldName <> "`"
       in [ "      const "
              <> fieldName
              <> " = "
              <> emitHostSerializer fieldType ("objectValue." <> fieldName) path
              <> ";"
          ]

    emitFieldReturn index =
      let fieldName = emitFieldName index
       in fieldName <> ": " <> fieldName

emitRecordCodecHelpers :: [TypeDecl] -> [RecordDecl] -> RecordDecl -> [Text]
emitRecordCodecHelpers typeDecls recordDecls recordDecl =
  [ "function " <> validateName <> "(value, path = \"value\") {"
  , "  const objectValue = $claspExpectObject(value, path);"
  ]
    <> concatMap emitValidateField (recordDeclFields recordDecl)
    <> [ "  return { " <> T.intercalate ", " (fmap (\fieldDecl -> recordFieldDeclName fieldDecl <> ": " <> recordFieldDeclName fieldDecl) (recordDeclFields recordDecl)) <> " };"
       , "}"
       , "function " <> validateInternalName <> "(value, path = \"value\") {"
       , "  const objectValue = $claspExpectObject(value, path);"
       ]
    <> concatMap emitValidateInternalField (recordDeclFields recordDecl)
    <> [ "  return { " <> T.intercalate ", " (fmap (\fieldDecl -> recordFieldDeclName fieldDecl <> ": " <> recordFieldDeclName fieldDecl) (recordDeclFields recordDecl)) <> " };"
       , "}"
       , "function " <> serializeName <> "(value) {"
       ]
    <> concatMap emitSerializeField (recordDeclFields recordDecl)
    <> [ "  return { " <> T.intercalate ", " (fmap (\fieldDecl -> recordFieldDeclName fieldDecl <> ": " <> recordFieldDeclName fieldDecl) (recordDeclFields recordDecl)) <> " };"
       , "}"
       , "function " <> validatePartialName <> "(value, path = \"value\") {"
       , "  const objectValue = $claspExpectObject(value, path);"
       , "  const partialValue = {};"
       ]
    <> concatMap emitValidatePartialField (recordDeclFields recordDecl)
    <> [ "  return partialValue;"
       , "}"
       , "function " <> validateInternalPartialName <> "(value, path = \"value\") {"
       , "  const objectValue = $claspExpectObject(value, path);"
       , "  const partialValue = {};"
       ]
    <> concatMap emitValidateInternalPartialField (recordDeclFields recordDecl)
    <> [ "  return partialValue;"
       , "}"
       , "function " <> serializePartialName <> "(value) {"
       , "  const partialValue = {};"
       ]
    <> concatMap emitSerializePartialField (recordDeclFields recordDecl)
    <> [ "  return partialValue;"
       , "}"
       , "function " <> decodeName <> "(jsonText) { return " <> validateName <> "(JSON.parse(jsonText), \"value\"); }"
       , "function " <> encodeName <> "(value) { return JSON.stringify(" <> serializeName <> "(" <> validateInternalName <> "(value, \"value\"))); }"
       , "function " <> decodePartialName <> "(jsonText) { return " <> validatePartialName <> "(JSON.parse(jsonText), \"value\"); }"
       , "function " <> encodePartialName <> "(value) { return JSON.stringify(" <> serializePartialName <> "(" <> validateInternalPartialName <> "(value, \"value\"))); }"
       , ""
       ]
  where
    validateName = "$validate_" <> recordDeclName recordDecl
    validateInternalName = "$validateInternal_" <> recordDeclName recordDecl
    serializeName = "$serialize_" <> recordDeclName recordDecl
    validatePartialName = "$validatePartial_" <> recordDeclName recordDecl
    validateInternalPartialName = "$validateInternalPartial_" <> recordDeclName recordDecl
    serializePartialName = "$serializePartial_" <> recordDeclName recordDecl
    decodeName = "$decode_" <> recordDeclName recordDecl
    encodeName = "$encode_" <> recordDeclName recordDecl
    decodePartialName = "$decodePartial_" <> recordDeclName recordDecl
    encodePartialName = "$encodePartial_" <> recordDeclName recordDecl

    emitValidateField fieldDecl =
      let fieldName = recordFieldDeclName fieldDecl
          fieldPath = "path + " <> emitStringLiteral ("." <> fieldName)
       in [ "  const " <> fieldName <> "Value = objectValue[" <> emitStringLiteral fieldName <> "];"
          , "  const " <> fieldName <> " = " <> emitValidator (recordFieldDeclType fieldDecl) (fieldName <> "Value") fieldPath <> ";"
          ]

    emitValidateInternalField fieldDecl =
      let fieldName = recordFieldDeclName fieldDecl
          fieldPath = "path + " <> emitStringLiteral ("." <> fieldName)
       in [ "  const " <> fieldName <> "Value = $claspRequireField(objectValue, " <> emitStringLiteral fieldName <> ", path);"
          , "  const " <> fieldName <> " = " <> emitInternalValidator (recordFieldDeclType fieldDecl) (fieldName <> "Value") fieldPath <> ";"
          ]

    emitSerializeField fieldDecl =
      let fieldName = recordFieldDeclName fieldDecl
       in [ "  const " <> fieldName <> " = " <> emitSerializer (recordFieldDeclType fieldDecl) ("value." <> fieldName) <> ";" ]

    emitValidatePartialField fieldDecl =
      let fieldName = recordFieldDeclName fieldDecl
          fieldPath = "path + " <> emitStringLiteral ("." <> fieldName)
       in [ "  if ($claspHasOwn(objectValue, " <> emitStringLiteral fieldName <> ")) {"
          , "    partialValue[" <> emitStringLiteral fieldName <> "] = " <> emitPartialValidator typeDecls recordDecls (recordFieldDeclType fieldDecl) ("objectValue[" <> emitStringLiteral fieldName <> "]") fieldPath <> ";"
          , "  }"
          ]

    emitValidateInternalPartialField fieldDecl =
      let fieldName = recordFieldDeclName fieldDecl
          fieldPath = "path + " <> emitStringLiteral ("." <> fieldName)
       in [ "  if ($claspHasOwn(objectValue, " <> emitStringLiteral fieldName <> ")) {"
          , "    partialValue[" <> emitStringLiteral fieldName <> "] = " <> emitInternalPartialValidator typeDecls recordDecls (recordFieldDeclType fieldDecl) ("objectValue[" <> emitStringLiteral fieldName <> "]") fieldPath <> ";"
          , "  }"
          ]

    emitSerializePartialField fieldDecl =
      let fieldName = recordFieldDeclName fieldDecl
       in [ "  if ($claspHasOwn(value, " <> emitStringLiteral fieldName <> ")) {"
          , "    partialValue[" <> emitStringLiteral fieldName <> "] = " <> emitPartialSerializer typeDecls recordDecls (recordFieldDeclType fieldDecl) ("value." <> fieldName) <> ";"
          , "  }"
          ]

emitForeignBindings :: [TypeDecl] -> [RecordDecl] -> [ForeignDecl] -> [Text]
emitForeignBindings typeDecls recordDecls foreignDecls =
  emitHostBindingManifest
    <> emitPackageBindingManifest
    <> fmap emitForeignBinding foreignDecls
    <> [""]
  where
    emitHostBindingManifest
      | null foreignDecls =
          [ "export const __claspHostBindings = [];"
          , "export const __claspPackageImports = [];"
          , "const __claspPackageBindings = Object.freeze({});"
          , "const $claspHostBindingMap = {};"
          , "export function __claspAdaptHostBindings(implementations = {}) {"
          , "  return {};"
          , "}"
          ]
      | otherwise =
          [ "export const __claspHostBindings = ["
          ]
            <> concatMap emitHostBindingEntry foreignDecls
            <> [ "];"
               , "export const __claspPackageImports = ["
               ]
            <> concatMap emitPackageImportEntry foreignDecls
            <> [ "];"
               , "const __claspPackageBindings = Object.freeze({"
               ]
            <> concatMap emitPackageBindingEntry foreignDecls
            <> [ "});"
               , "const $claspHostBindingMap = Object.fromEntries(__claspHostBindings.map((binding) => [binding.name, binding]));"
               , "export function __claspAdaptHostBindings(implementations = {}) {"
               , "  const runtimeBindings = {};"
               , "  for (const binding of __claspHostBindings) {"
               , "    const byName = implementations[binding.name];"
               , "    const byRuntimeName = implementations[binding.runtimeName];"
               , "    const implementation = typeof byName === \"function\" ? byName : byRuntimeName;"
               , "    if (typeof implementation === \"function\") {"
               , "      runtimeBindings[binding.runtimeName] = $claspAdaptHostBinding(binding, implementation);"
               , "    }"
               , "  }"
               , "  return runtimeBindings;"
               , "}"
               ]

    emitPackageBindingManifest =
      [ "export function __claspPackageHostBindings() {"
      , "  return __claspAdaptHostBindings(__claspPackageBindings);"
      , "}"
      ]

    emitHostBindingEntry foreignDecl =
      let (paramTypes, returnType) = splitFunctionType (foreignDeclType foreignDecl)
       in [ "  {"
          , "    name: " <> emitStringLiteral (foreignDeclName foreignDecl) <> ","
          , "    runtimeName: " <> emitStringLiteral (foreignDeclRuntimeName foreignDecl) <> ","
          ]
            <> emitHostBindingSource foreignDecl
            <> [ "    declaration: " <> emitHostBindingDeclaration foreignDecl <> ","
          , "    params: ["
          ]
            <> concat
              [ [ "      {"
                , "        index: " <> T.pack (show index) <> ","
                , "        type: " <> emitStringLiteral (renderTypeName paramType) <> ","
                , "        schema: " <> emitSchemaRef typeDecls recordDecls paramType <> ","
                , "        fromHost(value, path = \"value\") { return " <> emitHostDeserializer paramType "value" "path" <> "; },"
                , "        toHost(value, path = \"value\") { return " <> emitHostSerializer paramType "value" "path" <> "; }"
                , "      },"
                ]
              | (index, paramType) <- zip [(0 :: Int) ..] paramTypes
              ]
            <> [ "    ],"
               , "    returns: {"
               , "      type: " <> emitStringLiteral (renderTypeName returnType) <> ","
               , "      schema: " <> emitSchemaRef typeDecls recordDecls returnType <> ","
               , "      fromHost(value, path = \"result\") { return " <> emitHostDeserializer returnType "value" "path" <> "; },"
               , "      toHost(value, path = \"result\") { return " <> emitHostSerializer returnType "value" "path" <> "; }"
               , "    }"
               , "  },"
               ]

    emitHostBindingSource foreignDecl =
      case foreignDeclPackageImport foreignDecl of
        Nothing ->
          ["    source: null,"]
        Just packageImport ->
          [ "    source: {"
          , "      kind: " <> emitStringLiteral (emitPackageImportKind (foreignPackageImportKind packageImport)) <> ","
          , "      specifier: " <> emitStringLiteral (foreignPackageImportSpecifier packageImport)
          , "    },"
          ]

    emitHostBindingDeclaration foreignDecl =
      case foreignDeclPackageImport foreignDecl of
        Nothing ->
          "null"
        Just packageImport ->
          "{ path: "
            <> emitStringLiteral (foreignPackageImportDeclarationPath packageImport)
            <> ", signature: "
            <> maybe "null" emitStringLiteral (foreignPackageImportSignature packageImport)
            <> " }"

    emitPackageImportEntry foreignDecl =
      case foreignDeclPackageImport foreignDecl of
        Nothing ->
          []
        Just packageImport ->
          [ "  {"
          , "    name: " <> emitStringLiteral (foreignDeclName foreignDecl) <> ","
          , "    runtimeName: " <> emitStringLiteral (foreignDeclRuntimeName foreignDecl) <> ","
          , "    kind: " <> emitStringLiteral (emitPackageImportKind (foreignPackageImportKind packageImport)) <> ","
          , "    specifier: " <> emitStringLiteral (foreignPackageImportSpecifier packageImport) <> ","
          , "    declaration: " <> emitHostBindingDeclaration foreignDecl
          , "  },"
          ]

    emitPackageBindingEntry foreignDecl =
      case foreignDeclPackageImport foreignDecl of
        Nothing ->
          []
        Just _ ->
          [ "  " <> emitStringLiteral (foreignDeclName foreignDecl) <> ": " <> packageBindingIdentifier foreignDecl <> ","
          , "  " <> emitStringLiteral (foreignDeclRuntimeName foreignDecl) <> ": " <> packageBindingIdentifier foreignDecl <> ","
          ]

    emitForeignBinding foreignDecl =
      let params = foreignParams (foreignDeclType foreignDecl)
       in "function "
            <> emitIdentifier (foreignDeclName foreignDecl)
            <> "("
            <> T.intercalate ", " params
            <> ") { return $claspCallHostBinding("
            <> emitStringLiteral (foreignDeclName foreignDecl)
            <> ", ["
            <> T.intercalate ", " params
            <> "]); }"

