    [ "### " <> guideDeclName guideDecl
    , "- Extends: " <> maybe "none" id (guideDeclExtends guideDecl)
    ]
      <> case guideDeclEntries guideDecl of
        [] ->
          ["- Entries: none"]
        entries ->
          fmap renderGuideEntryDoc entries

renderGuideEntryDoc :: GuideEntryDecl -> Text
