collectStyledRefsDecl :: LowerDecl -> [Text]
collectStyledRefsDecl decl =
  case decl of
    LValueDecl _ expr ->
      collectStyledRefsExpr expr
    LFunctionDecl _ _ expr ->
      collectStyledRefsExpr expr

collectStyledRefsExpr :: LowerExpr -> [Text]
collectStyledRefsExpr expr =
  case expr of
    LPage title body ->
      collectStyledRefsExpr title <> collectStyledRefsExpr body
    LReturn value ->
      collectStyledRefsExpr value
    LViewText value ->
      collectStyledRefsExpr value
    LViewAppend left right ->
      collectStyledRefsExpr left <> collectStyledRefsExpr right
    LViewElement _ child ->
      collectStyledRefsExpr child
    LViewStyled styleRef child ->
      styleRef : collectStyledRefsExpr child
    LViewLink _ _ child ->
      collectStyledRefsExpr child
    LViewForm _ _ _ child ->
      collectStyledRefsExpr child
    LViewInput _ _ value ->
      collectStyledRefsExpr value
    LViewSubmit value ->
      collectStyledRefsExpr value
    LPromptMessage _ content ->
      collectStyledRefsExpr content
    LPromptAppend left right ->
      collectStyledRefsExpr left <> collectStyledRefsExpr right
    LPromptText promptExpr ->
      collectStyledRefsExpr promptExpr
    LList items ->
      concatMap collectStyledRefsExpr items
    LCall fn args ->
      collectStyledRefsExpr fn <> concatMap collectStyledRefsExpr args
    LConstruct _ fields ->
      concatMap collectStyledRefsExpr fields
    LMutableLet _ value body ->
      collectStyledRefsExpr value <> collectStyledRefsExpr body
    LAssign _ value body ->
      collectStyledRefsExpr value <> collectStyledRefsExpr body
    LFor _ iterable loopBody body ->
      collectStyledRefsExpr iterable <> collectStyledRefsExpr loopBody <> collectStyledRefsExpr body
    LMatch subject branches ->
      collectStyledRefsExpr subject <> concatMap (collectStyledRefsExpr . lowerMatchBranchBody) branches
    LRecord _ fields ->
      concatMap (collectStyledRefsExpr . lowerRecordFieldValue) fields
    LFieldAccess _ subject _ ->
      collectStyledRefsExpr subject
    _ ->
      []

lowerExprContainsReturn :: LowerExpr -> Bool
lowerExprContainsReturn expr =
  case expr of
    LVar _ ->
      False
    LInt _ ->
      False
    LString _ ->
      False
    LBool _ ->
      False
    LList items ->
      any lowerExprContainsReturn items
    LReturn _ ->
      True
    LEqual left right ->
      lowerExprContainsReturn left || lowerExprContainsReturn right
    LNotEqual left right ->
      lowerExprContainsReturn left || lowerExprContainsReturn right
    LLessThan left right ->
      lowerExprContainsReturn left || lowerExprContainsReturn right
    LLessThanOrEqual left right ->
      lowerExprContainsReturn left || lowerExprContainsReturn right
    LGreaterThan left right ->
      lowerExprContainsReturn left || lowerExprContainsReturn right
    LGreaterThanOrEqual left right ->
      lowerExprContainsReturn left || lowerExprContainsReturn right
    LLet _ value body ->
      lowerExprContainsReturn value || lowerExprContainsReturn body
    LMutableLet _ value body ->
      lowerExprContainsReturn value || lowerExprContainsReturn body
    LAssign _ value body ->
      lowerExprContainsReturn value || lowerExprContainsReturn body
    LFor _ iterable loopBody body ->
      lowerExprContainsReturn iterable || lowerExprContainsReturn loopBody || lowerExprContainsReturn body
    LPage title body ->
      lowerExprContainsReturn title || lowerExprContainsReturn body
    LRedirect _ ->
      False
    LViewEmpty ->
      False
    LViewText value ->
      lowerExprContainsReturn value
    LViewAppend left right ->
      lowerExprContainsReturn left || lowerExprContainsReturn right
    LViewElement _ child ->
      lowerExprContainsReturn child
    LViewStyled _ child ->
      lowerExprContainsReturn child
    LViewLink _ _ child ->
      lowerExprContainsReturn child
    LViewForm _ _ _ child ->
      lowerExprContainsReturn child
    LViewInput _ _ value ->
      lowerExprContainsReturn value
    LViewSubmit value ->
      lowerExprContainsReturn value
    LPromptMessage _ content ->
      lowerExprContainsReturn content
    LPromptAppend left right ->
      lowerExprContainsReturn left || lowerExprContainsReturn right
    LPromptText promptExpr ->
      lowerExprContainsReturn promptExpr
    LCall fn args ->
      lowerExprContainsReturn fn || any lowerExprContainsReturn args
    LConstruct _ fields ->
      any lowerExprContainsReturn fields
    LMatch subject branches ->
      lowerExprContainsReturn subject || any (lowerExprContainsReturn . lowerMatchBranchBody) branches
    LRecord _ fields ->
      any (lowerExprContainsReturn . lowerRecordFieldValue) fields
    LFieldAccess _ subject _ ->
      lowerExprContainsReturn subject

